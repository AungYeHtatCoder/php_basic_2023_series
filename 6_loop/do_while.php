<?php 
// do while loop -> loop with condition is true
// 1. initialize variable
// 2. code {} (do) 
// 3. condition (while)
// 4. increment or decrement variable 

$a = 0; // initialize variable
do {
  echo $a; // code
  echo "<br>";
  $a++; // increment or decrement variable
} while ($a <= 10); // condition